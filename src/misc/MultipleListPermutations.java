    static void permute(List<List<Integer>> lists, int listNo, int permutationSize, int n, List<Integer> permutation) {
        List<Integer> currentList = lists.get(listNo);
        for (int i = 0; i < currentList.size(); i++) {
            permutation.add(currentList.get(i));
            if (permutationSize == n - 1) {
                System.out.println(permutation);
                permutation.remove(permutationSize);
            } else {
                permute(lists, listNo + 1, permutationSize + 1, n, permutation);
                permutation.remove(permutationSize);
            }
        }
    }
